/*
    Classe com funções matemáticas simples para demonstrar testes baseados em propriedades
*/

public class MathFunctions {
    /*
      Multiplica um número por 2
      @param number o número a ser multiplicado
      @return o número multiplicado por 2
    */
    public static int multiplyByTwo(int number) {
        return number * 2;
    }
}